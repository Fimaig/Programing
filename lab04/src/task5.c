int main()
{
	#define X1 -0.5 //Задаємо значення X
	float y = 0; //Вводимо змінну y

	if ( X1 > 1 ){ //Якщо Х > 1 то
		y = X1 - 1;
	}
	else if ( X1 >= 0 && X1 <= 1 ){ //Якщо Х > або = 0 та < або = 1 то
		y = - X1 + 1;
	}
	else if ( X1 > -1 && X1 < 0 ){ //Якщо Х > -1 та < 0 то
		y = X1 + 1;
	}
	else if ( X1 < -1 ){ //Якщо Х < -1 то
		y = - X1 - 1;
	}
	return 0;
}
